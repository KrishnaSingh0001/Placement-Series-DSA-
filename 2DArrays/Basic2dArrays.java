public class Basic2dArrays {
    public static void main(String[] args) {
        int [][] grid = new int [3][5];
        // 1 2 3 
        // 4 5 6 
        // 7 8 9
        grid[0][0] = 1; 
        grid[0][1] = 2;
        grid[0][2] = 3;
        grid[1][1] = 4;
        grid[1][2] = 5;
        grid[1][3] = 6;
        grid[2][1] = 7;
        grid[2][2] = 8;
        grid[2][3] = 9;
        System.out.println(grid[2][2]);
            

    }
}
